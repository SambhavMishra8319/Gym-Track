const strength = (name, reps = "", weight = "") => ({
  name,
  type: "strength",
  sets: [{ reps, weight }],
});

const cardio = (name, time = "", extra = {}) => ({
  name,
  type: "cardio",
  sets: [{ time, distance: "", speed: "", incline: "", ...extra }],
});

const duration = (name, durationValue = "") => ({
  name,
  type: "duration",
  sets: [{ duration: durationValue }],
});

const carry = (name, distance = "", weight = "") => ({
  name,
  type: "carry",
  sets: [{ distance, weight }],
});

